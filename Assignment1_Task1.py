# Write a program to perform to Arithmetic Operation(+, _ ,*, /)
#Take the numbers from the user
n1= int(input("Enter the first number: "))
n2= int(input("Enter the second number: "))
#Adding two numbers using + operator
add = n1 + n2
print("Addition: ", add)
#Subtracting two numbers using - operator
sub = n1 - n2
print("Subtraction: ", sub)
#Multiplying two numbers using * operator
mul = n1 * n2
print("Multiplication: ", mul)
#Dividing two numbers using / operator
div = n1 / n2
print("Division: ", div)